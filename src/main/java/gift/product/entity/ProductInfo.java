package gift.product.entity;

public class ProductInfo {
    private String name;
    private int price;
    private String imageUrl;

    public ProductInfo(String name, int price, String imageUrl) {
        this.name = name;
        this.price = price;
        this.imageUrl = imageUrl;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public String getImageUrl() {
        return imageUrl;
    }
}
